class Identificadores {
    /*
    Ejemplo de literales validos
     */
    public static void main(String[] args) {
        String nombreUsuario    = "Carlos";
        String codInterno10     = "AAA0101";
        String _talla           = "XXL";

        int $sumaNumeros    = 60;
        int $105            = 105;
        int thisvar         = 10;
        String miVariable   = "compila";

        System.out.println("NombreUsuario: " + nombreUsuario);
        System.out.println("CodInterno10: " + codInterno10);
        System.out.println("_talla: " + _talla);
        System.out.println("$sumaNumeros: " + $sumaNumeros);
        System.out.println("$105: " + $105 + ", Thisvar: " + thisvar);
        System.out.println("MiVariable: " + miVariable);
    }
}
